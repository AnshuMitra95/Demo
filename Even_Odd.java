//Check given no. is even or odd
import java.util.*;
class Even_Odd
{
   public static void main(String a[])
    {
	   int n;
	   Scanner ob=new Scanner(System.in);
       System.out.println("Enter a number");
       n=ob.nextInt();
       if(n%2==0)
	        System.out.println("Even number");
	   else
             System.out.println("Odd number");
    }
}
	
         	   
